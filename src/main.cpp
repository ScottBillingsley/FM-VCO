/******************************************************************/
/**************************** Loop ********************************/
/******************************************************************/
void loop() {
  bounce = millis();


  if(next_sample){
    int32_t pwm_sample;
    int32_t temp_pwm;
    int32_t temp_mod;

        /* Add the mWord to the accumulator */
        mAccu += (int) mWord + fm_mod;
        modAccu += (int) modWord;
        /* Get the top 10 bits */
        mIcnt = (mAccu >> 6);
        modIcnt = (modAccu >> 6); 

    switch (selection)
    {
    case 0: /* Standard Osc */
        /* Get the sample from PROGMEM */
        pwm_sample = (int) pgm_read_word(&sine_wave[(int)mIcnt]);
        fm_mod = 0;
      break;  
    case 1: /* FM Osc */
        /* Get the sample from PROGMEM */
        pwm_sample = (int) pgm_read_word(&sine_wave[(int)mIcnt]);
        mod_sample = (int) pgm_read_word(&sine_wave[(int)modIcnt]);
        fm_mod = ((mod_sample << 3) * mod_amount) >> 8;
      break;
    case 2: /* AM Synthesis */
        /* Get the sample from PROGMEM */
        mod_sample = (int) pgm_read_word(&sine_wave[(int)modIcnt]);
        /* Normalize form bipolar +/- 512 to unipolar 0 to 256 */
        mod_sample = (127 + (mod_sample >> 2));
        temp_pwm = (int) pgm_read_word(&sine_wave[(int)mIcnt]);
        pwm_sample = (temp_pwm * mod_sample) >> 8;
        fm_mod = 0;
      break;
    case 3:  /* Ring Modulator */
        /* Get the sample from PROGMEM */
        temp_pwm = (int) pgm_read_word(&sine_wave[(int)mIcnt]);
        temp_mod = (int) pgm_read_word(&sine_wave[(int)modIcnt]);
        pwm_sample = (temp_mod * temp_pwm) >> 8;
        fm_mod = 0;
      break; 
        case 4:  /* XOR */
        /* Get the sample from PROGMEM */
        temp_pwm = (int) pgm_read_word(&sine_wave[(int)mIcnt]);
        temp_mod = (int) pgm_read_word(&sine_wave[(int)modIcnt]);
        pwm_sample = (temp_pwm ^ ((temp_mod * mod_amount) >> 8));
        fm_mod = 0;
      break; 
        case 5:  /* AND */
        /* Get the sample from PROGMEM */
        temp_pwm = (int) pgm_read_word(&sine_wave[(int)mIcnt]);
        temp_mod = (int) pgm_read_word(&sine_wave[(int)modIcnt]);
        pwm_sample = (temp_pwm & ((temp_mod * mod_amount) >> 8));
        fm_mod = 0;
      break;
        case 6:  /* OR */
        /* Get the sample from PROGMEM */
        temp_pwm = (int) pgm_read_word(&sine_wave[(int)mIcnt]);
        temp_mod = (int) pgm_read_word(&sine_wave[(int)modIcnt]);
        pwm_sample = (temp_pwm | ((temp_mod * mod_amount) >> 8));
        fm_mod = 0;
      break; 
        case 7:  /* HARM */
        /* Get the sample from PROGMEM */
        temp_pwm = (int) pgm_read_word(&sine_wave[(int)mIcnt]);
        temp_mod = (int) pgm_read_word(&sine_wave[(int)modIcnt]);
        pwm_sample = (temp_pwm + ((temp_mod * mod_amount) >> 8)) >> 1;
        fm_mod = 0;
      break;                    
    
    default:
      break;
    }

    PWM_OUT = (int) pwm_sample;

    if(PWM_OUT > 0 && !zero_crossed){
      zero_crossed = true;
      zero += 10;
    }
    if(PWM_OUT <= 0 && zero_crossed){
      zero_crossed = false;
    }
    if(zero > (1024 - adc_array[3])){
      zero = 0;
      LED_HIGH = !LED_HIGH;
      if(LED_HIGH){
        PORTD |= _BV (0);
      }else{
        PORTD &= ~_BV (0);
      }
    }

    next_sample = false;
  }

    /*Check to see if ADC has finished */
  if (!(bitRead(ADCSRA, ADSC)) && !next_sample) {
    /*Read and store the results  */
    byte temp_adcl = ADCL;
    int temp_adc = (ADCH << 8) + temp_adcl;
    /*Keep a running average */
    adc_array[adc_count] = (adc_array[adc_count] + temp_adc) / 2;
    /*Filter the results using an integer math low pass filter */
    filter(adc_count);
    /* The adc to service */
    adc_service = adc_count;
    /* Increment the adc count */
    adc_count ++;
    if(adc_count == ADC_MAX){
      adc_count = 0;
    }
    /* Change the ADC */
    chage_adc(adc_count);

    new_adc = true;
  }

  if(new_adc && !next_sample){
        uint32_t knob_temp;
        uint16_t this_mod;
        uint32_t this_mod_step;

        switch (adc_service)
    {
      case 0:
        /* Get the set freq from the freq knob  */
        knob_temp = pgm_read_word(&(pow2_times_512[(adc_filtered[0] >> 2)]));
        knob_mword = (base_mWord * knob_temp) >> 9;
        break;
      case 1:
          this_mod = adc_filtered[1]; 
          this_mod_step = pgm_read_word(&(pow2_times_512[this_mod]));
          switch(selection){
            case 1: /* FM */
              modWord = ((cv_mWord >> 3) * this_mod_step) >> 9;
            break;
            case 2: /* AM */
               modWord = ((cv_mWord >> 3) * (this_mod_step << 1)) >> 9; 
            break;
            case 3: /* RING */
               modWord = ((cv_mWord >> 3) * (this_mod_step << 1)) >> 9; 
            break;
            case 4: /* XOR */
               modWord = ((cv_mWord >> 3) * (this_mod_step << 1)) >> 9; 
            break;
            case 5: /* AND */
               modWord = ((cv_mWord >> 3) * (this_mod_step << 1)) >> 9; 
            break;
            case 6: /* OR  */
               modWord = ((cv_mWord >> 3) * (this_mod_step << 1)) >> 9; 
            break;
            case 7: /* HArmonic */
               modWord = ((cv_mWord >> 3) * (this_mod_step << 1)) >> 9; 
            break;             

            default:
            break;                                    
          }

        break;
      case 2:
        mod_amount = adc_filtered[2] >> 2;
        break;
      case 3:
        /* Calculate the CV word from the set knob */
        cv_mWord = (knob_mword * pgm_read_word(&(pow2_times_512[adc_array[3]]))) >> 9;
        break;                        

    }
    mWord = cv_mWord;
    new_adc = false;
  }

  /*******  Make the voice selection ******/
  if(!button_pressed){
    /* Check the state of the button */
    if((PING & 0x01)){
      selection ++;
      if(selection > 7){
        selection = 0;
      }
      PORTC = 0x01 << selection;
      old_millis = bounce + 400;
      button_pressed = true; 
    }
  }

  if(button_pressed){
    switch (selection)
    {
    case 0:
      data_array[8] = ALPHA_S;
      data_array[9] = ALPHA_T;
      data_array[10] = ALPHA_N;
      data_array[11] = ALPHA_D;
      break;
    case 1:
      data_array[8] = ALPHA_F;
      data_array[9] = ALPHA_M;
      data_array[10] = ALPHA_SPACE;
      data_array[11] = ALPHA_SPACE;
      break;
    case 2:
      data_array[8] = ALPHA_A;
      data_array[9] = ALPHA_M;
      data_array[10] = ALPHA_SPACE;
      data_array[11] = ALPHA_SPACE;
      break;
    case 3:
      data_array[8] = ALPHA_R;
      data_array[9] = ALPHA_I;
      data_array[10] = ALPHA_N;
      data_array[11] = ALPHA_G;
      break;
    case 4:
      data_array[8] = ALPHA_X;
      data_array[9] = ALPHA_O;
      data_array[10] = ALPHA_R;
      data_array[11] = ALPHA_SPACE;
      break;
    case 5:
      data_array[8] = ALPHA_A;
      data_array[9] = ALPHA_N;
      data_array[10] = ALPHA_D;
      data_array[11] = ALPHA_SPACE;
      break;
    case 6:
      data_array[8] = ALPHA_O;
      data_array[9] = ALPHA_R;
      data_array[10] = ALPHA_SPACE;
      data_array[11] = ALPHA_SPACE;
      break; 
    case 7:
      data_array[8] = ALPHA_H;
      data_array[9] = ALPHA_A;
      data_array[10] = ALPHA_R;
      data_array[11] = ALPHA_M;
      break;            
    
    default:
      break;
    }
  }

  if(bounce > old_millis){
    if(!(PING & 0x01)){
      button_pressed = false;
    }
  }

  /************** The LCD Display *******************/
  switch (LCD_state)
  {
  case 0:
      /*Set the address for print */
      /*Set the port for the control address byte */
      /*Set bit 7 to tell the 1602 that this is an address byte */
      LCD_CONTROL_IO = bitSet(address_array[array_step], 7);
      /*Select control register */
      LCD_RS_LOW;
      /*Set to write mode  */
      LCD_RW_LOW;
    break;
  case 1:
      /*Take LCD enable low then high to execute the command */
      LCD_EN_HIGH;
    break; 
  case 2:
      LCD_EN_LOW;
      /*Select DATA register */
      LCD_RS_HIGH;
    break;
  case 3:
      /*Set the port for the display byte */
      LCD_DATA_IO = data_array[array_step];
  
      LCD_EN_HIGH;
    break;
  case 4:
        LCD_EN_LOW;
        /* Step thru the display array */
        array_step ++;
        if(array_step > 31){
          array_step = 0;
        }
    break;   
  
  default:
    break;
  }

    LCD_state ++;
    if (LCD_state > 4) {
      LCD_state = 0;
    }


}/*************************** End Loop *****************************/